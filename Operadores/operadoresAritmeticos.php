<?php

/**
 * Operadores Arítmeticos
 */

 $numero1 = 2;
 $numero2 = 3;

 // Suma
$suma = $numero1 + $numero2;

echo 'Suma -> ' . $suma . PHP_EOL;

 // Resta
$resta = $numero2 -  $numero1;

echo 'Resta -> ' . $resta . PHP_EOL;

 // Multiplicacion
$multiplicacion = $numero1 * $numero2;

echo 'Multiplicacion -> ' . $multiplicacion . PHP_EOL;

 // División
$divisionNumero1 = 4;
$divisionNumero2 = 2;

$divison = $divisionNumero1 / $divisionNumero2;

echo 'Division -> ' .  $divison . PHP_EOL;

 // Potencia -> Podemos hacer la potencia elevada de un número 2^3
$potencia = $numero1 ** $numero2;

echo 'Potencia -> ' . $potencia . PHP_EOL;

 // Módulo -> Nos da el resto de una división
$modulo = $divisionNumero1 % $divisionNumero2;

echo 'Modulo -> ' . $modulo . PHP_EOL;
